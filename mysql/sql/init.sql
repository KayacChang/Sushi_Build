CREATE TABLE IF NOT EXISTS `attach` (
  `PlayerID` bigint NOT NULL,
  `Kind` bigint NOT NULL,
  `Type` bigint NOT NULL,
  `IValue` bigint NOT NULL,
  PRIMARY KEY (`PlayerID`,`Kind`,`Type`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8;

